package edu.scripps.pms.census.tmtFilter;

public class ProteinRatio {

	private int index = -1;
	
	private String ratioHeader = null;
	
	
	private String rationValue = null;


	public int getIndex() {
		return index;
	}


	public void setIndex(int index) {
		this.index = index;
	}


	public String getRatioHeader() {
		return ratioHeader;
	}


	public void setRatioHeader(String ratioHeader) {
		this.ratioHeader = ratioHeader;
	}


	public String getRationValue() {
		return rationValue;
	}


	public void setRationValue(String rationValue) {
		this.rationValue = rationValue;
	}
	
	
}
