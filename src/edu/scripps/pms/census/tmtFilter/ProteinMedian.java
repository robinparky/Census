package edu.scripps.pms.census.tmtFilter;

public class ProteinMedian {

	private int index = -1;
	
	private String medianHeader = null;
	
	
	private String medianValue = null;

	public String getMedianHeader() {
		return medianHeader;
	}

	public void setMedianHeader(String medianHeader) {
		this.medianHeader = medianHeader;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public String getMedianValue() {
		return medianValue;
	}

	public void setMedianValue(String medianValue) {
		this.medianValue = medianValue;
	}
	
	
}
